            <p>
                Há alguns anos atrás Hélio Batalha, estava insatisfeito com sua carreira. Sentia que havia uma grande lacuna entre as empresas que precisavam de funcionários e os candidatos que estavam em busca de emprego. Ele viu muitos casos de empresas que tinham dificuldades em encontrar funcionários qualificados, ao mesmo tempo, muitos candidatos que tinham dificuldades em encontrar oportunidades que correspondessem às suas habilidades e interesses.</p> 

